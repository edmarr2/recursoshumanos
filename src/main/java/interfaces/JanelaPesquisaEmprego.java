    private Timestamp getDataAdmissao(JTextField data) {
        String dataAdmissaoStr = data.getText();
        Timestamp dataAdmissao = null;

        if (!dataAdmissaoStr.isEmpty()) {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            try {
                Date parsedDate = dateFormat.parse(dataAdmissaoStr);
                dataAdmissao = new Timestamp(parsedDate.getTime());
            } catch (ParseException e) {
                e.printStackTrace();
            }
        }
        return dataAdmissao;
    }

    private void mostrarEmpregosSelecionados(ArrayList<Emprego> empregos) {
        StringBuilder resultados = new StringBuilder();

        for (Emprego emprego : empregos) {
            if (resultados.length() > 0) {
                resultados.append("\n");
            }
            resultados.append(emprego.toStringFull());
        }

        resultadosPesquisaTextArea.setText(resultados.toString());
    }

